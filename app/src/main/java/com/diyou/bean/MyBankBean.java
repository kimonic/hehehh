package com.diyou.bean;

public class MyBankBean
{
    private String name;
    private String account_full;
    private String account;
    private String bank_small;

    public String getBank_small()
    {
        return bank_small;
    }

    public void setBank_small(String bank_small)
    {
        this.bank_small = bank_small;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getAccount_full()
    {
        return account_full;
    }

    public void setAccount_full(String account_full)
    {
        this.account_full = account_full;
    }

    public String getAccount()
    {
        return account;
    }

    public void setAccount(String account)
    {
        this.account = account;
    }
}
